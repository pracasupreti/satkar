<br>

<span style="font-weight: bold;">सेवा प्रदायकहरू</span><br>

<br>


तपाइँलाई हाम्रो सेवाहरू प्रदान गर्नको लागि, हामी तेस्रो पक्ष कम्पनीहरू र व्यक्तिहरू ("सेवा प्रदायकहरू") प्रयोग गर्न सक्दछौं, जसले हाम्रो तर्फबाट सेवा प्रदान गर्दछ, सेवा-सम्बन्धित सेवाहरू प्रदर्शन गर्न वा हाम्रो सेवा कसरी प्रयोग भइरहेको छ भन्ने विश्लेषण गर्न हामीलाई मद्दत गर्दछ ।<br>
<br>
हाम्रो तर्फबाट सेवाहरू प्रदान गर्न, यी तेस्रो पक्षहरूको तपाईंको व्यक्तिगत डाटामा पहुँच हुन सक्दछ तर अन्य कुनै उद्देश्यका लागि खुलासा वा प्रयोग नगर्न बाध्य छन्।<br>
<br>

<span style="font-weight: bold;">अन्य साइटहरू</span><br>

<br>

हाम्रो सेवाले अन्य साइट लिंकहरू समावेश गर्न सक्दछ जुन हामी द्वारा संचालित हुँदैन । यदि तपाइँ तेस्रो पक्षको लिंकमा क्लिक गर्नुहुन्छ भने हामी त्यसलाई तेस्रो पक्षको साइटको रूपमा सन्दर्भ गर्दछौं । हामी तपाईंलाई त्यस साइटमा गोपनीयता नीति बुझ्न निर्देशन दिन्छौं ।<br>

<br>

त्यस्ता साइटहरूमा हामीसँग कुनै नियन्त्रण छैन र उनीहरूले पोष्ट गरेको सामग्री, गोपनीयता नीति वा उनीहरूको अभ्यासको लागि हामी जिम्मेवार हुँदैनौं ।<br>

<br>

<span style="font-weight: bold;">बच्चाहरूको गोपनीयता</span><br>

<br>

हामी १८ वर्ष भन्दा कम उमेरका&nbsp;बच्चाहरूलाई संलग्न हुन दिदैनौं&nbsp;। हामी जानाजानी १८ बर्ष मुनिका कसैबाट कुनै पनि व्यक्तिगत विवरणहरू संकलन गर्दैनौं, यो आफ्ना अभिभावकको आफ्ना बच्चाहरूलाई सचेत गराउनु कर्तव्य हो । यदि बच्चाहरू द्वारा व्यक्तिगत डेटा सेयरिंग फेला पर्नुभयो भने तपाईं हामीलाई सम्पर्क गर्न सक्नुहुनेछ । यदि हामी सचेत भयौं कि हामीले अभिभावकको प्रमाणीकरण बिना बच्चाहरूबाट व्यक्तिगत डेटा&nbsp;सङ्कलन गरेका छौं भने, हामी हाम्रा सर्वरहरूबाट जानकारी हटाउनको लागि आवश्यक कदमहरू चाल्नेछौं ।<br>


<br>


<span style="font-weight: bold;">यस गोपनीयता नीतिमा परिवर्तन</span><br>


<br>


हामी समय समयमा हाम्रो गोपनीयता नीति नियमित रूपमा अपडेट गर्दछौं । यदि हाम्रो गोपनीयता नीतिको बारेमा कुनै संशोधन हुनुपर्दछ, हामी तपाईंलाई यस पृष्ठमा पोस्ट गरेर सूचित गर्नेछौं।<br>

<br>

हामी तपाईंलाई ईमेल मार्फत परिवर्तनहरू र / वा हाम्रो सेवामा प्रमुख सूचनाको बारेमा सूचित गराउँदछौं, वास्तविक परिवर्तनहरू प्रभावकारी हुनु अघि र यस गोपनीयता नीतिको माथि "प्रभावी मिति" पनि अपडेट गर्नेछौं।<br> 

<br>

हामी तपाईलाई गोपनीयता परिवर्तन कुनै समय परिवर्तनको लागि समय समयमा समीक्षा गर्न सिफारिस गर्दछौं । यस पृष्ठमा पोस्ट गरेपछि, गोपनीयता नीति पोस्ट गरिएको मितिबाट प्रभावी हुनेछ।<br>

<br>

