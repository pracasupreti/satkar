श्रीयोग&nbsp;कन्सल्टिंग प्राइभेट लिमिटेडले तपाईको व्यक्तिगत लगतलाई राम्रो आस्थाका साथ खुलासा गर्न सक्छ कि त्यस्तो कार्य आवश्यक छ:<br>

<br>

&nbsp;&nbsp;&nbsp;&nbsp; * कानुनी दायित्व पूरा गर्दै<br>
&nbsp;&nbsp;&nbsp;&nbsp; * श्रीयोग&nbsp;कन्सल्टिंग प्रा.लि. को अधिकार र सम्पत्तीको सुरक्षा गर्दै<br>
&nbsp;&nbsp;&nbsp;&nbsp; * सेवाको सम्बन्धमा सम्भावित गलत कार्यको रोकथाम वा अनुसन्धान<br>
&nbsp;&nbsp;&nbsp;&nbsp; * सेवा वा सार्वजनिक प्रयोगकर्ताहरूको व्यक्तिगत सुरक्षाको संरक्षण<br>
&nbsp;&nbsp;&nbsp;&nbsp; * कानुनी दायित्व बिरूद्ध संरक्षण डाटाको सुरक्षा<br>
<br>
