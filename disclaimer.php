<p style="font-weight: bold;">उपयोग जानकारी डेटा</p>


<p>हामी मानिसहरूले कसरी हाम्रा सेवाहरू प्रयोग गर्छन् ("प्रयोग डेटा") भन्ने बिषयमा जानकारीहरु लिन्छौ । यस डेटाले विभिन्न जानकारीहरू समावेश गर्दछ जस्तै तपाईंको कम्प्युटर आईपी, ब्राउजर विवरणहरू, सेवाहरूको पृष्ठ, तपाईंले पढ्ने र हेर्ने पृष्ठहरू, तपाईंको संलग्नता, यूडीआईडी इत्यादि।</p>

<p>ट्र्याकिंग र कुकीज डेटा</p>

<p>कुकीहरू र समान ट्राकिंग टेक्नोलोजीहरूको प्रयोग गरेर हामी हाम्रो सेवाहरूको गतिविधिहरू ट्र्याक गर्छौं र केही जानकारी राख्छौं।</p>

<p>कुकीज डाटाको सानो मात्रासँग फाईलहरू हुन् जसले अज्ञात अद्वितीय पहिचानकर्ताहरू समावेश गर्न सक्दछन् र एक वेबसाईटबाट तपाईंको ब्राउजरहरूमा पठाइन्छ र तपाईले प्रयोग गर्ने मोबाईल फोन, ल्यापटप, ट्याब्लेट डिभाइस आदिमा  भण्डारण गरिन्छ । ट्र्याकिंग टेक्नोलोजीहरूले हाम्रो सेवाहरू सुधार गर्न मद्दत ट्र्याक गर्न जानकारी सङ्कलन गर्न ट्याग र स्क्रिप्टहरू प्रयोग गर्दछ ।</p>

<p>तपाइँको ब्राउजरलाई सबै कुकीहरू अस्वीकार गर्न वा कुकीहरू निगरानी गर्न निर्देशन दिईन्छ। जहाँसम्म, यदि तपाइँ कुकिजहरू स्वीकार गर्नुहुन्न भने, तपाइँ हाम्रा सेवाहरूको केहि अंशहरू प्रयोग गर्न सक्नुहुन्न जुन तपाइँको उपलब्ध कुकिजमा निर्भर हुन सक्छ ।</p>


