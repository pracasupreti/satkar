<p>तपाईंको डाटा र प्रतिक्रिया प्रयोग गरेर हामी सेवा अपग्रेड गर्नेछौं । pahunaGhar.org प्रयोग गरेर हामी यो मान्दछौं कि तपाईं हाम्रो नीति अनुसार जानकारीको सङ्कलन र प्रयोगमा विश्वास गर्नुहुन्छ र सहमत हुनुहुन्छ । यस गोपनीयता नीतिमा परिभाषित शर्तहरूको हाम्रो अर्थ र शर्तहरू यस गोपनीयता नीतिमा परिभाषित नगरेसम्म समान अर्थ राख्दछ ।</p>


