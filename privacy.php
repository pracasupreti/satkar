<p>तपाइँको गोपनीयता नीतिसँग तपाइँको सहमति सहित तपाइँले यस प्रकारको जानकारी पेश गर्नुको मतलब तपाइँ त्यो स्थानान्तरणसँग सहमत हुनुहुन्छ।</p>

<p>श्रीयोग कन्सल्टिंग प्रा.लि. लिमिटेडले तपाईको डेटा सुरक्षित छ भनेर सुनिश्चित गर्न सबै आवश्यक कदमहरू चाल्नेछ र यस गोपनीयता नीति अनुसार र तपाइँको डेटा र अन्य व्यक्तिगत जानकारीको सुरक्षा सुनिश्चित गर्नु अघि कुनै संगठन वा देशमा तपाइँको व्यक्तिगत लगत स्थानान्तरण हुने छैन।</p>

