            }, 2e3)
        }

        function setNavigation() {
            var path = window.location.pathname;
            $(".secondary-nav-menu li a").each(function() {
                var href = $(this).attr("href");
                if (path === href) {
                    $(this).closest("li").addClass("active")
                }
            })
        }
