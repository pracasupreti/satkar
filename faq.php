        function openTab() {
            var params = getUrlVars();
            $('#wf-tab a[href="#' + params["tab"] + '"]').tab("show");
            $("#" + params["tab"] + " #" + params["q"]).collapse();
            if ($("#" + params["q"]).length > 0) $("html, body").animate({
